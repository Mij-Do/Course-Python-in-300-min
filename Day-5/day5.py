# Variable Names
# 1- اهم حاجة تسمية المتغير يدل علي القيمة اللي فيه .
name = "Ahmed";
print(name); # output ==> Ahmed

#####################################################

# 2- مينفعش تبدا اسم المتغير برقم (1name) ده غير صحيح وهيطلع error
# SyntaxError: invalid decimal literal
# بس ينفع يكون الرقم تاني خانة او اخر خانة في الكلمة (n2ame) (name1)

# 1name = "Ahmed" ==> SyntaxError
n2ame = "Ahmed"; # ==> Ahmed
name2 = "Ahmed"; # ==> Ahmed

######################################################

